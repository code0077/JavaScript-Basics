/*
    Javascript type coercion
*/

console.log("5" + 1);
console.log("5" - 1);
console.log(8 * null);
console.log(null * 8);
console.log(1 + "5");
console.log(-1 + "5");
console.log(8 * "five");
console.log(false == 0);
console.log(false === 0);
console.log(null == 0);
console.log(null === 0);
console.log("" == false);
console.log(NaN == false);
console.log(" " == false);
